    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // Igual
        String resultado;
        memoria2=TxtPantalla.getText();

        try
        {
            // Operaciones Aritméticas + x^y
            if ((operacion=="+")||(operacion=="-")||(operacion=="*")||(operacion=="/")||(operacion=="potencia"))
            {
                resultado = Aritmeticas(memoria1, memoria2, operacion);
                TxtPantalla.setText(resultado);
            }
                 
            /* Otras operaciones:
             * Trigonométricas y Exponenciales
             */
            else if ((operacion=="Cos")||(operacion=="√")||(operacion=="alcuadrado")||(operacion=="eª")||(operacion=="Sin")||(operacion=="Tan")||(operacion=="Ln")||(operacion=="Log10")||(operacion=="Arcsin")||(operacion=="Arccos")||(operacion=="Arctan")||(operacion=="Abs")||(operacion=="Cbrt")||(operacion=="Arcsin"))
            {
                resultado = TrigsYExp(operacion, memoria2);
                TxtPantalla.setText(resultado);
            }                      
        }
        catch (java.lang.NullPointerException e)
        {
            memoria2 = "0";
        }
    }//GEN-LAST:event_btnIgualActionPerformed

