    public String TrigsYExp (String operacion, String memoria1)
    {
        double resultado=0.0;
        String respuesta;
        
        switch(operacion)
        {
            case "√":
                resultado= Math.sqrt(Double.parseDouble(memoria1));
                break;
 
            case "alcuadrado":
                resultado = Math.pow(Double.parseDouble(memoria1), 2);
                break;
                
            case "Sin":
                resultado = Math.round(Math.sin(Math.PI*(Double.parseDouble(memoria1))/180));
                break;
                
            case "Cos":
                resultado = Math.round(Math.cos(Math.PI*(Double.parseDouble(memoria1)/180)));
                break;
                
            case "Tan":
                resultado = Math.round(Math.tan(Math.PI*(Double.parseDouble(memoria1)/180)));
                break;
                
            case "eª":
                resultado = Math.exp(Double.parseDouble(memoria1));
                break;
                
            case "Ln":
                resultado = Math.log(Double.parseDouble(memoria1));            
                break;
            
            case "Log10":
                resultado = Math.log10(Double.parseDouble(memoria1));            
                break;
            
            case "Arcsin":
                resultado = Math.asin(Double.parseDouble(memoria1));
                break;
            
            case "Arccos":
                resultado = Math.acos(Double.parseDouble(memoria1));
                break;
                
            case "Arctan":
                resultado = Math.atan(Double.parseDouble(memoria1));
                break;
            
            case "Abs":
                resultado = Math.abs(Double.parseDouble(memoria1));
                break;
                
            case "Cbrt":
                resultado = Math.cbrt(Double.parseDouble(memoria1));
                break;
        }
        respuesta=String.valueOf(resultado);
        return respuesta;
    }
    
