    public /*static*/ String Aritmeticas (String memoria1, String memoria2, String operacion)
    {
        //Double resultado=0.0;
        //^Aquí 'resultado' no es un dato de tipo <double>: es un objeto de la clase 'Double'.
         
        double resultado=0.0;
        String respuesta;
        
        switch(operacion)
        {
            case "-": 
                resultado= Double.parseDouble(memoria1)-Double.parseDouble(memoria2);
                break;
                
            case "+":
                resultado= Double.parseDouble(memoria1)+Double.parseDouble(memoria2);
                break;
                
            case "/":
                resultado= Double.parseDouble(memoria1)/Double.parseDouble(memoria2);
                break;
                
            case "*":
                resultado= Double.parseDouble(memoria1)*Double.parseDouble(memoria2);
                break;
                
            case "potencia":
                resultado= Math.pow(Double.parseDouble(memoria1), Double.parseDouble(memoria2));
                break;
                
        }
        //respuesta=resultado.toString();
        /* ^Como 'resultado' es un objeto, y no un dato de tipo <double>, sí aplica el método .toString()
           para convertirlo a tipo String, y así poder publicarlo en TxtPantalla.
         * Sin embargo, por algún motivo el IDE lo marcaba con error (a pesar de que si servía).
         * Así que usar el método String.valueOf(<var de tipo double>) también funcionó (y sin marcar e-
           rror).
         */
        respuesta=String.valueOf(resultado);
        return respuesta;
    }
    
